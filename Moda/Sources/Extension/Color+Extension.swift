  static let monthlyEmpty: Color = Color(.monthlyEmpty)
  static let optionActive: Color = Color(.optionActive)
  static let optionBg: Color = Color(.optionBg)
  static let optionInactive: Color = Color(.optionInactive)
  static let previousDay: Color = Color(.previousDay)
  static let previousDayBg: Color = Color(.previousDayBg)
  static let today: Color = Color(.today)
  static let todayBg: Color = Color(.todayBg)
  static let todo: Color = Color(.todo)
  static let todoTitle: Color = Color(.todoTitle)
}
