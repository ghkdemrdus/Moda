import UIKit

extension UIColor {
    class var dateBg: UIColor { return UIColor(named: "DateBackground")!}
    class var previousDate: UIColor { return UIColor(named: "PreviousDate")!}
    class var followingDate: UIColor { return UIColor(named: "FollowingDate")!}
    class var currentDate: UIColor { return UIColor(named: "CurrentDate")!}
}
